				<h2>Christian René Wold</h2>
				<p>
					En gang lå verden for Christian René Wolds føtter (født i 1972). Den skoleflinke
					vestkantgutten kombinerer økonomistudier med talenter på scenen.
				</p>
			</div>
		</div>
		<p>
			En høy og veltrent kropp gjør underverker for selvtilliten, modellkarrieren og
			skuespillerambisjonene. I 1997 reiser han til New York for å realisere drømmer. Han hadde
			begynt å få prøvespille til større filmroller når en ryggprolaps gjorde at han må reise hjem
			til Norge for å operere. Legen ga ham seks ukers forbud mot å sitte. Seks uker etter
			operasjonen, setter han seg på flyet til New York. Men der venter avslag fra agenten, fordi
			han ikke lenger ser like veltrent ut.
		</p>
		<p>
			Drømmen ble knust. Smertestillende mot ryggvondt fikk følge av stadig mer alkohol, kokain og
			ecstasy. Wold reiste hjem til Oslo, rettere sagt til Oslos underverden, der han var ble en
			tung og farlig kriminell i sju år. Likevel ble han bare tatt av politiet to ganger. Dessverre,
			sier han selv i dag. I 2006 ble han dømt og fikk en lang straff. Da han ble satt inn for å
			sone hadde han fått en datter som da var et halvt år.
		</p>
		<i>
			– Jeg ble tatt av politiet og fikk på håndjern inne på Oslo City med barnevogn. Herregud, så
			flau jeg var! Jeg fikk en barnevernssak på meg, og fikk en veldig bra saksbehandler. Hun sa
			til meg: Christian, hvis dette skjer en eneste gang til, så kan du ikke bo sammen med barnet
			ditt.
		</i>
		<p>
			Christian René Wold var en fryktet figur i Oslos underverden. Men i fengselet innså han at han
			måtte kutte ut rusen og begynte å lese bøker. Det forandret livet hans.
		</p>
		<p>
			Fengselsdommen og datteren reddet han fra rus og kriminalitet, og han begynte å studere.
			Fortsatt trives han best på gata, men nå som sosionom. Og forfatter. For han begynte å skrive.
			Foreløpig er det blitt tre krimromaner, skrevet av en mann som vet hva han snakker om. Som
			kjenner kriminelle miljøer. Og som utsatte ungdommer stoler på. Et nytt liv.
		</p>
