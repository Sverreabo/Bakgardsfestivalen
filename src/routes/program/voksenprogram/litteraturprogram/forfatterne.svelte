	<div id="Jonas Bendiksen">
		<div class="relative">
			<figure>
				<img width="715" height="480" alt="Jonas Bendiksen" src="/Jonas Bendiksen.jpg" />
				<figcaption>
					Jonas Bendiksen født i 1977 i Tønsberg er knyttet til Magnum Photos, et kollektiv av
					fotografer etablert i 1947. Bendiksens bilder er blant annet publisert i National
					Geographic, GEO, Newsweek, Vanity Fair og The Sunday Times.
				</figcaption>
			</figure>
			<div class="header-img">
				<h2 id="bendiksen-h2">Jonas Bendiksen</h2>
			</div>
		</div>
		<p>
			Han startet sin karriere som 19-åring som praktikant ved London-kontoret til Magnum Photos, og
			er i dag fullverdig medlem der.
		</p>
		<p>
			Han presenterte sju års arbeid i tidligere Sovjetunionen i boka <i>Satellites</i>, som var
			hans første bok og kom ut i 2006 på engelsk, fransk, italiensk og nederlandsk. I 2008 kom han
			med sin andre bok, som kom ut på engelsk og norsk med tittelen <i>The Places We Live</i>. Her
			dokumenterer han menneskers liv i slumområder rundt om i verden: Kibera i Nairobi, Dharavi i
			Mumbai. Los Barrios i Caracas og Kampung Miskin i Jakarta. I 2018 ga Bendiksen ut boka
			<i>Det Siste Testament</i>, hvor han følger 7 menn som påstår å være Messias. Dette ble også
			til en TV-serie vist på NRK.
		</p>
		<p>
			Boken «Book of Veles», som kom i 2021, var noe helt annet enn det den ga seg ut for. Jonas
			Bendiksen avslørte eget juks. «Det har vært to år med bedrag. Dette er gjort som et forsvar
			for journalistikken,» sier han. «Det er et forsøk på vekke meg selv og bransjen til hva som
			faktisk kommer til å velte over oss i stort format de neste årene».
		</p>
		<p>Hjemme på Nesodden laget Jonas fotoboka «Book of Veles».</p>
		<p>
			Fotoprosjektet ble hyllet og applaudert ved lanseringen, men det viste seg at det var noe helt
			annet enn det utga seg for å være.
		</p>
		<p>
			Tiden skrus tilbake til 2016, og øynene vendes mot byen Veles. <b>Donald Trump</b> hadde vunnet
			det amerikanske presidentvalget og beskyldninger om «fake news» haglet. Verdens øyne vendtes mot
			Veles, en by nord i Makedonia. Den viste seg å være et episenter for produksjonen av falske nyheter,
			nyheter som var med på å avgjøre det amerikanske valget. Store internasjonale og nasjonale medier
			reiste dit for å rapportere.
		</p>
		<p>
			Hjemme i Norge satt Jonas Bendiksen og fulgte med, forferdet over hva som skjedde med mediene,
			med nyhetskonseptet og med sannheten. Han begynte å grave i historien til Veles, og et par år
			senere landet han selv i byen.
		</p>
		<p>
			Da han først reiste til Veles i 2019 møtte han ingen som faktisk hadde noe med historien om
			Veles å gjøre. Han fotograferte rom, bygninger og landskap, ventet til de var tomme for
			mennesker før han dro opp kamera og før han plasserte et 360-graders kamera i det samme
			rommet, det samme landskapet. Han hadde nemlig en plan.
		</p>
		<p>
			Bendiksen er en av Norges mest kjente fotografer, medlem av det velkjente fotobyrået Magnum og
			har laget flere suksessfulle fotobøker. Han har etablert seg som en seriøs fotojournalist som
			tar historiefortelling, og ikke minst fotografiet, på alvor.
		</p>

		<p>
			Det var ingen som mistenkte at boka han slapp i april 2021 ikke hadde hold i virkeligheten.
			Ingen så at menneskene på bildene var <i>avatarer</i>, datagenererte figurer nøye plassert inn
			i bildene, lyssatt og påkledd av Bendiksen selv.
		</p>
		<p>
			«Jeg har tukla med alle bildene i boka,» sier han. «Det eneste som ikke er tukla med er de
			reelle sitatene fra de falske nyhetssidene. Men de er jo også bare vissvass».
		</p>
		<p>
			Essayteksten i boka, som han har fått mye skryt for, er en autogenerert tekst laget av en
			datamaskin den også.
		</p>
		<p>
			Nå har han gjort rede for de fleste forfalskningene, men ikke alle. Det er flere der. Boka er
			full av hint. Det er mange steder det er lagt ut røde flagg i boka. For å gjøre det helt
			tydelig; det var aldri meningen at det ikke skulle ut i dagen.
		</p>
		<p>
			Da han slapp boka og begynte å selge den, tenkte han at det ikke kom til å ta lang tid før han
			ble avslørt. Han la også ut en del av bildene på Instagram. Men den første responsen ble ikke
			som ventet.
		</p>
		<p>
			Det skjedde ingenting, og han fikk flere og flere flotte tilbakemeldinger fra folk. De sa ting
			som: «Så bra gjort, takk for at du tar tak i disse viktige temaene».
		</p>
		<p>Ingen fattet mistanke. Boka solgte og lovordene fortsatte å komme.</p>
