			Afghanistan på reportasjetur og flyttet dit. Siden den gang har han skrevet for norske aviser,
			og også laget dokumentarer for NRK Brennpunkt. I 2010 ble Hammer utnevnt til Årets frilanser,
			og i 2011 ble han tildelt Fritt Ord-prisen for sin uavhengige og kritiske dekning av krigen i
			Afghanistan. I 2014 fikk han Den store journalistprisen og i 2017 og 2020 Internasjonal
			Reporters journalistpris. I 2018 fikk han pris for beste film i kategorien nyheter og politikk
			i The Webby Awards i New York for dokumentaren Escape from Syria. I 2020 laget han filmen Do
			Not Split hvor han følger demonstrasjonene i Hongkong. Filmen vant en rekke priser og ble
			nominert til Oscar i 2021 for beste kortdokumentar.
